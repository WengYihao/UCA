package com.cn.uca.bean;

public class PopularityBean {

    private String pic ;
    private String name;
    private int count;
    private int sum;

    public String getPic() {
        return pic;
    }

    public String getName() {
        return name;
    }

    public int getCount() {
        return count;
    }

    public int getSum() {
        return sum;
    }

    public void setPic(String pic) {
        this.pic = pic;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public void setSum(int sum) {
        this.sum = sum;
    }
}
